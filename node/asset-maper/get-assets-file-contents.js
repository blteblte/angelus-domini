const toCamelCase = require('camelcase');

const getAssetsFileContents = (className, assets) => {
  let props = assets.reduce((str, asset) =>
    str += `\n  ${toCamelCase(asset)} = require('./${asset}');`
  , '')
  const data = `/*\n  @autogenerated\n  run 'npm run a' to refresh files\n*/\nexport class ${className} {${props}\n}\n`
  return data;
}

module.exports.getAssetsFileContents = getAssetsFileContents
